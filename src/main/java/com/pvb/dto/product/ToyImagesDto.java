package com.pvb.dto.product;

public class ToyImagesDto {
	private int id;
	private String url;
	private Long toyId;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public Long getToyId() {
		return toyId;
	}
	public void setToyId(Long toyId) {
		this.toyId = toyId;
	}
	
	
}
